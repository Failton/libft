int		ft_atoi(char *str)
{
	int a[2];
	int num;

	num = 0;
	a[0] = 0;
	a[1] = 0;
	while ((str[a[1]] > 8 && str[a[1]] < 14) || str[a[1]] == 32)
		a[1]++;
	while (str[a[1]] == 45 || str[a[1]] == 43)
	{
		if (str[a[1]] == 45)
			a[0]++;
		a[1]++;
	}
	while (str[a[1]] > 47 && str[a[1]] < 58)
	{
		num = num * 10 + str[a[1]] - '0';
		a[1]++;
	}
	if (a[0] % 2 == 1)
		num *= -1;
	return (num);
}
